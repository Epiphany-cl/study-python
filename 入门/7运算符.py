# python 有整除
print("1 / 2 :::", 1 / 2)
print("1 // 2 :::", 1 // 2)

# ** 代表幂运算
print("2 ** 3 :::", 2 ** 3)

# not 关键字 相当于 Java 中的 !
print("not True :::", not True)

# a if condition else b 类似于 java 的三元运算符 (condition) ? a : b
print("'小明' if False else '小红' :::", '小明' if False else '小红')

# Python 多变量赋值
a, b = 1, 2

# is None 用于对象是否为空 类似于 java 的 == null
print("(not None) is None :::", (not None) is None)

# python 中 in 可以检查成员关系
print("'a' in ['a', 'b', 'c'] :::", 'a' in ['a', 'b', 'c'])
